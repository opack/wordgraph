package com.slamdunk.wordgraph.puzzle;

public enum PuzzleTypes {
	/**
	 * Type pour les puzzles qui proposent de découvrir plusieurs mots
	 * indépendants
	 */
	WORDS,
	/**
	 * Type pour les puzzles qui proposent de découvrir les mots d'une
	 * phrase, telle qu'une citation, un dicton ou une phrase culte.
	 */
	SENTENCE
}
